package JavaTotorials;

public class findingArrayElementUsingWithForLoop {

	public static void main(String[] args) {

		String[] bros = { "vamsi", "vishnu", "ravindra", "manohar" };

		for (int i = 0; i < bros.length; i++) {
			// System.out.println(i);
			System.out.println(bros[i]);

		}

	}

}
