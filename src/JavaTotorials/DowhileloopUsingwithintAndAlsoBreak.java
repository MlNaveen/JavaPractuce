package JavaTotorials;

public class DowhileloopUsingwithintAndAlsoBreak {

	public static void main(String[] args) {

		int i = 1;

		do {
			System.out.println(i);
			System.out.println("Hello");
			i++;
		}

		while (i <= 20);

		System.out.println("End of The Loop");

	}

}
