package JavaTotorials;

public class checkinginArraylengthUsingWithForloop {

	public static void main(String[] args) {

		String[] frinds = { "Manoj", "Rajkumar", "Raj", "Shiva" };

		for (int i = 0; i < frinds.length; i++)

		{
			// array variable lo una elements ni count chestundhi
			System.out.println(i);

			// array variable lo una elements ni display chestundhi
			System.out.println(frinds[i]);

		}

	}

}
